                    <!-- Actions -->
                    <div class="ml-4 flex-shrink-0 flex space-x-2">
                      <a :href="'/storage/' + medium.path" target="_blank" class="inline-flex items-center px-3 py-1 border border-gray-300 shadow-sm text-sm leading-4 font-medium rounded-md text-gray-700 bg-white hover:bg-gray-50 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-indigo-500">
                        View
                      </a>
                      <inertia-link
                        :href="route('admin.media.edit', medium.id)"
                        class="inline-flex items-center px-3 py-1 border border-gray-300 shadow-sm text-sm leading-4 font-medium rounded-md text-gray-700 bg-white hover:bg-gray-50 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-indigo-500">
